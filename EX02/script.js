let number1 = parseInt(prompt("Digite um numero: "))
let number2 = parseInt(prompt("Digite outro numero: "));

let soma = number1 + number2;
let subtracao = number1 - number2;
let mulplicacao = number1 * number2;
let divisao = number1 / number2;

alert(`Resultados:
${number1} + ${number2} = ${soma}
${number1} - ${number2} = ${subtracao}
${number1} * ${number2} = ${mulplicacao}
${number1} / ${number2} = ${divisao}
`)